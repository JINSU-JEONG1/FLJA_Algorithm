n = int(input())
plans = input().split()

x, y = 1, 1 # 시작 위치 

dx = {'L': 0, 'R': 0, 'U': -1, 'D': 1}
dy = {'L': -1, 'R': 1, 'U': 0, 'D': 0}

for p in plans:
    nx = x + dx[p]
    ny = y + dy[p]

    # 공간을 벗어나지 않는 경우에만 
    if 1 <= nx <= n and 1 <= ny <= n:
        x, y = nx, ny

print(x, y)



# [문제 요약 - 상하좌우]
# 여행가 A는 N x N 크기의 정사각형 공간에 있으며,
# 시작 위치는 항상 (1, 1)이다.
# 여행가는 계획서에 따라 상(U), 하(D), 좌(L), 우(R)로
# 한 칸씩 이동할 수 있다.
#
# 공간의 좌표 범위는 (1, 1)부터 (N, N)까지이며,
# 이 범위를 벗어나는 이동은 무시한다.
#
# 계획서는 공백으로 구분된 문자들의 나열로 주어지며,
# 각 문자는 다음을 의미한다:
# L: 왼쪽 이동, R: 오른쪽 이동, U: 위로 이동, D: 아래로 이동
#
# [구현 포인트]
# - 현재 위치 (x, y)를 저장한다.
# - 각 이동 명령마다 이동 후 좌표가
#   1 ≤ x ≤ N, 1 ≤ y ≤ N 인지 확인한다.
# - 범위를 벗어나는 이동은 수행하지 않는다.
#
# [예시]
# N = 5
# 계획서: R R R U D D
# 이동 경로:
# (1,1) → (1,2) → (1,3) → (1,4) → (1,4) → (2,4) → (3,4)
# 최종 위치: (3, 4)