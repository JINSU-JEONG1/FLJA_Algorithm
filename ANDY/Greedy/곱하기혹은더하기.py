s = input().strip() 

result = int(s[0])

""" 처음에는 문자열을 바로 숫자로 바꾸려고 했음. 예를 들어서 "02984" 가 있으면 바로 int() 로 
숫자로 바꾸려고 했지만 그러면 한 덩어리의 숫자가 돼서 그냥 for _ in _ 으로 하나씩 뽑아서 
그걸 비교 하기로 했다"""
for ch in s[1:]:
    digit = int(ch)

    if result <= 1 or digit <= 1:
        result += digit
    else:
        result *= digit

print(result)

# strip은 그냥 넣었음. 문자열 양쪽의 공백을 제거하는 함수임
# 첫 번째 숫자는 연산 없이 그대로 시작값으로 사용
# 두 번째 문자부터 하나씩 처리
# 둘 중 하나라도 0 또는 1이면 더하는 것이 유리

# [문제 요약 - 곱하기 혹은 더하기]
# 숫자(0~9)로만 이루어진 문자열 S가 주어진다.
# 문자열을 왼쪽부터 차례대로 확인하면서
# 각 숫자 사이에 '+' 또는 '×' 연산자를 하나씩 넣어
# 최종적으로 만들 수 있는 가장 큰 수를 구하는 문제이다.
#
# 단, 일반적인 연산 우선순위는 없으며
# 모든 연산은 왼쪽에서 오른쪽으로 순차적으로 수행된다.
#
# [핵심 아이디어 (그리디)]
# 현재까지의 계산 결과(result)와 다음 숫자(digit)를 비교했을 때,
# 둘 중 하나라도 0 또는 1이면 곱셈은 이득이 되지 않으므로 더하기를 선택한다.
# 둘 다 2 이상인 경우에만 곱셈이 더 큰 결과를 만든다.
#
